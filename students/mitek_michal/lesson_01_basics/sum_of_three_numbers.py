
def print_sum_of_three_numbers():

    first_number = int(input("Provide first number "))
    second_number = int(input("Provide second number "))
    third_number = int(input("Provide third number "))

    sum_of_three_numbers = first_number + second_number + third_number
    print(sum_of_three_numbers)

    return sum_of_three_numbers


print_sum_of_three_numbers()
