# Area of right-angled triangle

# Statement
# Write a program that reads the length of the base and the height of a
# right-angled triangle and prints the area. Every number is given on a separate line.

print("Enter the length of the base")
l = int(input())
print("Enter the height")
h = int(input())

area = (l * h) / 2

print("Area: " + str(area))
